# 在字符串中找到正则表达式所匹配的所有子串，并把它们作为一个迭代器返回
import re

print(re.finditer('11', '11,22,11,12,12,112,11111'))
r = re.finditer('11', '11,22,11,12,12,112,11111')
for i in r:
    print(i)
